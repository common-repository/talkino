/**
 * The admin-specific functionality of the plugin.
 *
 * Defines the plugin name, version, and two hooks to
 * enqueue the admin-facing stylesheet and JavaScript.
 *
 * @package    Talkino
 * @subpackage Talkino/includes/admin
 * @author     Traxconn <mail@traxconn.com>
 */
class Talkino_Admin {

	/**
	 * The ID of this plugin.
	 *
	 * @since     1.0.0
	 * @access    private
	 * @var       string     $plugin_name    The ID of this plugin.
	 */
	private $plugin_name;

	/**
	 * The unique prefix of this plugin.
	 *
	 * @since     1.0.0
	 * @access    private
	 * @var       string     $plugin_prefix    The string used to uniquely prefix technical functions of this plugin.
	 */
	private $plugin_prefix;

	/**
	 * The version of this plugin.
	 *
	 * @since     1.0.0
	 * @access    private
	 * @var       string     $version    The current version of this plugin.
	 */
	private $version;

	/**
	 * Initialize the class and set its properties.
	 *
	 * @since    1.0.0
	 * @param    string $plugin_name      The name of this plugin.
	 * @param    string $plugin_prefix    The unique prefix of this plugin.
	 * @param    string $version          The version of this plugin.
	 */
	public function __construct( $plugin_name, $plugin_prefix, $version ) {

		$this->plugin_name   = $plugin_name;
		$this->plugin_prefix = $plugin_prefix;
		$this->version       = $version;

	}

	/**
	 * Register the stylesheets for the admin area.
	 *
	 * @since    1.0.0
	 * @param    string $hook_suffix    The current admin page.
	 */
	public function enqueue_styles( $hook_suffix ) {

		global $current_screen;

		if ( 'talkino_agents' === $current_screen->post_type ) {

			wp_enqueue_style( 'talkino-admin', plugin_dir_url( TALKINO_BASE_NAME ) . 'assets/css/talkino-admin.css', array(), $this->version, 'all' );

			// Enqueue dashicons picker for admin area.
			wp_enqueue_style( 'dashicons-picker', plugin_dir_url( TALKINO_BASE_NAME ) . 'assets/css/dashicons-picker.css', array( 'dashicons' ), $this->version, 'all' );

		}

	}

	/**
	 * Register the JavaScript for the admin area.
	 *
	 * @since    1.0.0
	 * @param    string $hook_suffix    The current admin page.
	 */
	public function enqueue_scripts( $hook_suffix ) {

		global $current_screen;

		if ( 'talkino_agents' === $current_screen->post_type ) {

			wp_enqueue_script( 'talkino-admin', plugin_dir_url( TALKINO_BASE_NAME ) . 'assets/js/talkino-admin.js', array( 'jquery' ), $this->version, false );

			// Add the color picker css file.
			wp_enqueue_style( 'wp-color-picker' );

			// Make sure to add the wp-color-picker dependecy to js file.
			wp_enqueue_script( 'color-picker', plugin_dir_url( TALKINO_BASE_NAME ) . 'assets/js/color-picker.js', array( 'jquery', 'wp-color-picker' ), $this->version, true );

			// Enqueue jquery for sorting.
			wp_enqueue_script( 'jquery-ui-sortable', false, array( 'jquery-ui-core', 'jquery' ), $this->version, true );

			// Enqueue jquery for dashicons picker.
			wp_enqueue_script( 'dashicons-picker', plugin_dir_url( TALKINO_BASE_NAME ) . 'assets/js/dashicons-picker.js', array( 'jquery' ), $this->version, true );

			// Enqueue jquery for talkino report.
			wp_enqueue_script( 'talkino-report', plugin_dir_url( TALKINO_BASE_NAME ) . 'assets/js/talkino-report.js', array( 'jquery' ), $this->version, true );

			// Pass $php_vars array to javascript as php object for channel ordering.
			$ajax_url = array( 'ajax_url' => admin_url( 'admin-ajax.php' ) );
			wp_localize_script( 'talkino-admin', 'talkino_admin_ajax_object', $ajax_url );

		}

	}

}
